const storageKeys = {
  users: "careerCompassUsers",
  session: "careerCompassSession",
  language: "careerCompassLang"
};

function getText(key, lang) {
  return translations[lang][key] || key;
}

function applyLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    element.textContent = getText(key, lang);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    element.placeholder = getText(key, lang);
  });

  localStorage.setItem(storageKeys.language, lang);
}

async function hashPassword(password) {
  const encoder = new TextEncoder();
  const data = encoder.encode(password);
  const digest = await crypto.subtle.digest("SHA-256", data);
  return Array.from(new Uint8Array(digest)).map((byte) => byte.toString(16).padStart(2, "0")).join("");
}

function setMessage(text, isError = false) {
  const messageBox = document.getElementById("messageBox");
  if (!messageBox) return;
  messageBox.textContent = text;
  messageBox.style.color = isError ? "#ff8aa1" : "#67f2c4";
}

function readUsers() {
  return JSON.parse(localStorage.getItem(storageKeys.users) || "{}") || {};
}

function saveUsers(users) {
  localStorage.setItem(storageKeys.users, JSON.stringify(users));
}

function createSession(user) {
  const session = {
    userEmail: user.email,
    expiresAt: Date.now() + 15 * 60 * 1000,
    token: crypto.randomUUID()
  };
  localStorage.setItem(storageKeys.session, JSON.stringify(session));
}

function getSession() {
  const session = JSON.parse(localStorage.getItem(storageKeys.session) || "null");
  if (!session) return null;
  if (session.expiresAt && session.expiresAt < Date.now()) {
    localStorage.removeItem(storageKeys.session);
    return null;
  }
  return session;
}

function isAuthenticated() {
  return Boolean(getSession());
}

function logout() {
  localStorage.removeItem(storageKeys.session);
  window.location.href = "index.html";
}

async function handleLogin(event) {
  event.preventDefault();
  const email = document.getElementById("loginEmail").value.trim();
  const password = document.getElementById("loginPassword").value;
  const users = readUsers();
  const user = users[email];
  if (!user) {
    setMessage("No account found for that email.", true);
    return;
  }

  const passwordHash = await hashPassword(password);
  if (passwordHash !== user.passwordHash) {
    setMessage("Incorrect password. Please try again.", true);
    return;
  }

  createSession(user);
  setMessage("Secure login successful.");
  window.location.href = "dashboard.html";
}

async function handleRegister(event) {
  event.preventDefault();
  const name = document.getElementById("registerName").value.trim();
  const email = document.getElementById("registerEmail").value.trim();
  const password = document.getElementById("registerPassword").value;

  if (password.length < 8) {
    setMessage("Password must be at least 8 characters.", true);
    return;
  }

  const users = readUsers();
  if (users[email]) {
    setMessage("That email is already registered.", true);
    return;
  }

  const passwordHash = await hashPassword(password);
  users[email] = {
    email,
    name,
    passwordHash,
    createdAt: new Date().toISOString()
  };
  saveUsers(users);
  setMessage("Account created successfully. Please sign in.");
  document.getElementById("registerForm").reset();
}

function renderDashboard() {
  const session = getSession();
  if (!session) {
    window.location.href = "index.html";
    return;
  }

  const users = readUsers();
  const user = users[session.userEmail];
  if (!user) {
    logout();
    return;
  }

  const welcomeName = document.getElementById("welcomeName");
  const summary = document.getElementById("dashboardSummary");
  if (welcomeName) {
    welcomeName.textContent = `Welcome back, ${user.name}`;
  }
  if (summary) {
    summary.textContent = `Your account is secure and ready for guidance on streams, careers, and coaching centers.`;
  }
}

function setupAuthHandlers() {
  const loginForm = document.getElementById("loginForm");
  const registerForm = document.getElementById("registerForm");
  const logoutBtn = document.getElementById("logoutBtn");

  if (loginForm) {
    loginForm.addEventListener("submit", handleLogin);
  }
  if (registerForm) {
    registerForm.addEventListener("submit", handleRegister);
  }
  if (logoutBtn) {
    logoutBtn.addEventListener("click", logout);
  }
}

function setupLanguageSwitcher() {
  const select = document.getElementById("languageSelect");
  if (!select) return;
  const savedLang = localStorage.getItem(storageKeys.language) || "en";
  select.value = savedLang;
  applyLanguage(savedLang);
  select.addEventListener("change", (event) => {
    applyLanguage(event.target.value);
  });
}

function setupSessionTimeout() {
  let timeout;
  const resetTimer = () => {
    clearTimeout(timeout);
    timeout = setTimeout(() => logout(), 15 * 60 * 1000);
  };

  ["mousemove", "keydown", "scroll", "click"].forEach((eventName) => {
    window.addEventListener(eventName, resetTimer, { passive: true });
  });
  resetTimer();
}

function init() {
  setupLanguageSwitcher();
  setupAuthHandlers();
  if (window.location.pathname.includes("dashboard.html")) {
    renderDashboard();
    setupSessionTimeout();
  } else if (isAuthenticated()) {
    window.location.href = "dashboard.html";
  }
}

document.addEventListener("DOMContentLoaded", init);
